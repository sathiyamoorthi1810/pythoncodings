#special operators
#identity operators(is, is not)
#is
#true
name = "python"
name1 = "python"
print(name is  name1)
#false
name = "python"
name1 = "java"
print(name is  name1)

#is not
#flase
name = "python"
name1 = "python"
print(name is not name1)
#true
name = "python"
name1 = "java"
print(name is  name1)

#membership operators (in , not in)
#in
#flase
name = "sakthi"
print("chennai" in name)

#true
name = "sakthi"
print("sakthi" in name)

#not in
# false
name = "sakthi"
print("sakthi" in name)

#true
name = "sakthi"
print("chennai" in name)


